-- Make jumping between lines easier
vim.opt.number = true
vim.opt.relativenumber = true

-- Use spaces instead of tabs 
vim.opt.expandtab = true

-- Set the number of spaces that a <Tab> counts for
vim.opt.tabstop = 2

-- Set the number of spaces to use for each step of (auto)indent
vim.opt.shiftwidth = 2

-- Set the number os spaces that a <Tab> counts for while editing
vim.opt.softtabstop = 2

-- Makes searches case-insensitive
vim.opt.ignorecase = true

-- Makes searches case-sensitive if the search string contains uppercase letters
vim.opt.smartcase = true

vim.opt.autoindent = true
